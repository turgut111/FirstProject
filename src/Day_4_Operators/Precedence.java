package Day_4_Operators;

public class Precedence {
    public static void main(String[] args) {

        int result= 5+3*8-7;
        System.out.println(result);

        int x=5;

        x +=1; // x=x+1;
        System.out.println(x);

        x -=7; // x=x-7;

        x *=-5;// x=x*-5;



    }
}
